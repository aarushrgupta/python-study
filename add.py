# The program should ask for two numbers.
# and and 3 numbers will add, subtract, multiply, and print the result.
number1 = int(input("pick your first number: "))
number2 = int(input("pick your second number: "))
number3 = int(input("pick your third number: "))

print("addition of these numbers is:", number1+number2+number3)
print("multiplication of these numbers is:", number1*number2*number3)
print("subtraction of these numbers is:", number1-number2-number3)
print("division of these numbers is:", number1/number2/number3)


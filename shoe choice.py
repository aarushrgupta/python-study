def shoe_choice(weather):
    if weather == "snowy":
        print("wear your snow boots.")
    elif weather == "cool":
        print("wear your tennis shoes.")
    elif weather == "hot":
        print("Wear your sandals.")
            

print("when it's snowing...")
shoe_choice("snowy")

print("when it's hot...")
shoe_choice("hot")

print("when it's cool...")
shoe_choice("cool")
